						<br/> Hadi Pakzad, is one of the first singers who started rock and alternative music composing in Iran. He was born
						in December 1982 in Iran, Mashhad. He got his bachelor degree in civil engineering from university of Mashhad.
						<br/> He took harmonic and music lessons with (Keyvan alaee), He also took Piano and Guitar lessons from (Naser Tofighi).
						Before he got involved with music,he was interested in classical poetry and philosophical article writing. In the year
						2000, after he was trained in the field of Piano and Guitar, he started to produce his first album in a home studio
						by help of some local musicians. The songs of his album were inspired by his poems. Due to lack of cultural atmosphere
						he released his firs album named (Fear Land) and distributed it in parties. In no time, his distinct style became popular
						throughout the country and among Iranian who lived abroad. Then, with the support of his fans, he organized some underground
						concerts in Tehran and other major cities. Since his songs were received warmly by the fans, he decided to rerecord
						his album by the involvement of a professional team and new facilities and noticeable capital in a studio in Tehran.
						He also decided to do some advertising and release his artistic work on his own site.
						<br/> In the year 2005 he held two official and huge concerts in Mashhad, before the distribution of his new work.
						Then he released his new album named (Living Under Ground) in August 2006. 18GB of his songs were downloaded from internet
						by fans in the first few days, in the meantime, his videoclip named (Daryad), was broadcasted from satellite channels
						such as PMC.

						<br/>
						<br/> One of his mentionable characteristic traits is his sense of generosity. The acquired benefit of his artistic
						activities was given to charities. In one of his interviews with a European radio (Radio Zamaneh), the given reason
						for his avoidance of presence in legal and official music marketing was the factor of Choice. He said: "In underground
						system I have the chance to choose my audience, whereas; in legal and official system I only have the chance of being
						chosen".
						<br/> In one of his concerts he was introduced to a young and talented musician named (Masoud Fayyaz Zadeh). His cooperation
						with this person led to the creation of some outstanding works. After a while, all Hadi had to do was coming up with
						a Theme, and Masoud was doing the rest.
						<br/> His new albume Doctor, will be the result of Masoud's music composing and Hadi's lyrics. They are working on
						it and it will be released in one month.
						<br/> Hadi's favorite singers are Deep Purple, and Bryan Adams. Most of his poems are imaginable and in the form of
						short and ambiguous plays. He says: "Directing videoclips is something that i'm following", he adds: "After the release
						of my last album Doctor, I intend to produce and direct some videoclips so that I can present image and plays according
						to my own taste".
						<br/> 2000 to 2004: University of Mashhad, Civil Engineering Studied harmony under supervision of (Sahhaf),(Naser Tofighi)
						and (Keyvan Alaee).
						<br/> 1990 to 2001: Took Guitar and Piano lessons, with (Keyvan Alaee).
						<br/> Doctor (2007)
